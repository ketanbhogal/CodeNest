public class WideningTypeCast2 {
    public static void main(String[]args){
    
        //TypeCast  To Float
        //Implicite TypeCasting
    
        byte a=40;
        float b=a;
        System.out.println(b);
    
        //Explicite TypeCasting
        byte a1=44;
        float b1=(float)a1;
        System.out.println(b1);
    
    
    
    
        }
        
    }
    
    
    
    

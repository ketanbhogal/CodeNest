public class switchcase {
    public static void main(String[]args){
        int num=6;

        switch(num){
            case 1:{
                System.out.println("This Number Is Match With 1");
            }
            break;
            case 2:{
                System.out.println("This Number Is Match With 2");
            }
            break;
            case 3:{
                System.out.println("This Number Is Match With 3");
            }
            break;
            case 4:{
                System.out.println("This Number Is Match With 4");
            }
            break;
            case 5:{
                System.out.println("This Number Is Match With 5");
            }
            break;
            case 6:{
                System.out.println("This Number Is Match With 6");
            }
            break;
            case 7:{
                System.out.println("This Number Is Match With 7");
            }
            break;
            case 8:{
                System.out.println("This Number Is Match With 8");
            }
            break;
            default:{
                System.out.println("This Is No Match With Any Number Please Enter Valid Number");
            }
        }
    }
    
}




public class WideningTypeCast {
    public static void main(String[]args){

        //TypeCast Int To Float
        //Widening is convert Low Type Of Data Into Higher Type Of Data
         //Implicite TypeCasting

        int a=10;
        float b=a;
        System.out.println(b);

        //Explicite TypeCasting
        int a1=10;
        float b1=(float)a1;
        System.out.println(b1);




    }
    
}




